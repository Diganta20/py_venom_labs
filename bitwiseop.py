#there are total 6 of bitwise op
#1. bitwise AND (&)
#2. bitwise OR (|)
#3. bitwise XOR (^)
#4. bitwise NOT (~)
#5. left shift (<<)
#6. right shift (>>)

#1 &
#The bitwise AND operator (&) compares each bit of the first operand to the corresponding bit of th
#e second operand. If both bits are 1, the corresponding result bit is set to
#1. Otherwise, the corresponding result bit is set to 0.
#Example: 5 & 3 = 1

#2 or
#The bitwise OR operator (|) compares each bit of the first operand to the corresponding bit of
# the second operand. If either bit is 1, the corresponding result bit is set to
#1. Otherwise, the corresponding result bit is set to 0.


print(5 & 3)  # output: 1
print(5|3) 
print(5^3)
print(~5)
print(10<<5)
print(10>>5)  # output: 1